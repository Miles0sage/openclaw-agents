"""Generate Markdown report from benchmark suite results."""

from benchmarks.runner import SuiteResult



def generate_markdown(suite: SuiteResult) -> str:
    lines = [
        f"# OpenClaw Benchmark Report: {suite.suite_name}",
        f"**Date:** {suite.timestamp[:10]}",
        "",
        "## Summary",
        "",
        "| Metric | Value |",
        "|--------|-------|",
        f"| Total problems | {suite.total} |",
        f"| Passed | {suite.passed} |",
        f"| Failed | {suite.failed} |",
        f"| **Pass rate** | **{suite.pass_rate:.0%}** |",
        f"| Avg quality score | {suite.avg_score:.3f} |",
        f"| Total cost | ${suite.total_cost_usd:.4f} |",
        f"| Total time | {suite.total_duration_sec:.1f}s |",
        "",
        "## By Difficulty",
        "",
        "| Difficulty | Total | Passed | Rate |",
        "|------------|-------|--------|------|",
    ]

    for diff in ["easy", "medium", "hard"]:
        if diff in suite.by_difficulty:
            d = suite.by_difficulty[diff]
            lines.append(f"| {diff} | {d['total']} | {d['passed']} | {d['pass_rate']:.0%} |")

    lines += [
        "",
        "## By Category",
        "",
        "| Category | Total | Passed | Rate |",
        "|----------|-------|--------|------|",
    ]
    for cat, d in sorted(suite.by_category.items()):
        lines.append(f"| {cat} | {d['total']} | {d['passed']} | {d['pass_rate']:.0%} |")

    lines += [
        "",
        "## Detailed Results",
        "",
        "| Problem | Difficulty | Score | Passed | Cost | Time | Files | Agent |",
        "|---------|-----------|-------|--------|------|------|-------|-------|",
    ]

    for r in suite.results:
        status = "PASS" if r.passed else "FAIL"
        icon = "✅" if r.passed else "❌"
        lines.append(
            f"| {r.problem_id} | {r.difficulty} | {r.overall_score:.2f} | "
            f"{icon} {status} | ${r.cost_usd:.4f} | {r.duration_sec:.1f}s | "
            f"{r.file_checks_passed}/{r.file_checks_total} | {r.agent_key} |"
        )

    lines += ["", "---", "Generated by OpenClaw Benchmark Suite"]
    return "\n".join(lines)
